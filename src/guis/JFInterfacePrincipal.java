    private void jBRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBRemoverActionPerformed
        if (jTRemove.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Insira os dados corretamente");
            return;
        }
        
        if (lista.isVazio()) {
           jTListaJogos.setText("");
           JOptionPane.showMessageDialog(rootPane, "impossivel remover. Lista Vazia");
           return;
        }
        
        Jogo jogo = new Jogo (jTRemove.getText(), "","","", "");
        if(lista.remover(jogo) == null) {
            JOptionPane.showMessageDialog(rootPane, "Jogo nao encontrado");
            jTRemove.setText("");
            return;
        }
        
        atualizaVisualizacao();
        jTRemove.setText("");
    }//GEN-LAST:event_jBRemoverActionPerformed

    private void jBInsereInicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBInsereInicioActionPerformed
        if (jTNome.getText().trim().isEmpty()
                || jTGenero.getText().trim().isEmpty()
                || jTProdutora.getText().trim().isEmpty()
                || jTAno.getText().trim().isEmpty()
                || jTPlataforma.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Insira os dados corretamente");
            return;
        }
        Jogo jogo = new Jogo(
                jTNome.getText(),
                jTGenero.getText(),
                jTProdutora.getText(),
                jTAno.getText(),
                jTPlataforma.getText()
        );
        lista.insereNoInicio(jogo);
        atualizaVisualizacao();
        limpaCampos();
    }//GEN-LAST:event_jBInsereInicioActionPerformed

